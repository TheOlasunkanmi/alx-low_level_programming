#include <stdio.h>
/**
 * main - print if the number is base ten starting from zero
 *
 * Description: using the main function, this programme prints
 * all single digit numbers of base 10 starting from 0
 * Return: 0
 */
int main(void)
{
	int i;

	for (i = 0; i < 10; i++)
	{
		printf("%i", i);
	}
	printf("\n");
	return (0);
}

